export function useTranslation() {
  const { preferences } = useAuth();
  const lang = preferences?.language === "en-US" ? "en-US" : "es-ES";

  const t = (key, defaultText) => {
    return translations[lang]?.[key] || defaultText || key;
  };

  return { t, lang };
}
